args<-commandArgs(trailingOnly = FALSE)
i<-read.csv(args[length(args)-1])

png(filename=args[length(args)], width=800)
options(scipen=20)
plot(NULL,xlim=c(0,length(i$table_cache)),ylim=c(0,ymax=max(i$table_cache)*1.25),xaxs="i", yaxs="i", ylab="", xlab="", main="Files and Tables",las=1,xaxt="n", type="h")
rect(0,0,length(i$table_cache),max(i$table_cache),col="lightgray")
lines(i$open_tables, col="blue")
lines(i$open_files, col="orange")
lines(diff(i$opened_tables), col="red")
legend("top", ncol=2, legend=c("Table Cache Size", "Open Tables", "Open Files", "Opened Tables"), col=c("lightgray","blue","orange","red"), lty=1,lwd=2, bty="n")
dev.off()
