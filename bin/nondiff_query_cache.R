args<-commandArgs(trailingOnly = FALSE)
i<-read.csv(args[length(args)-1])

qh<-(i$hits)
qi<-(i$inserts)
qlp<-(i$lowmem_prunes)
qnc<-(i$not_cached)
qic<-(i$queries_in_cache)

png(filename=args[length(args)], width=800)
options(scipen=20)
plot(NULL,xlim=c(0,length(qh)),ylim=c(0,ymax=max(qh,qi,qlp,qnc,qic)*1.25),xaxs="i", yaxs="i", ylab="", xlab="", main="Query Cache",las=1,xaxt="n", type="h")
lines(qh, col="blue")
lines(qi, col="orange")
lines(qlp, col="green")
lines(qnc, col="purple")
lines(qic, col="red")
legend("top", ncol=2, legend=c("Hits", "Inserts", "Lowmem Prunes", "Not Cached", "In Cache"), col=c("blue","orange","green","purple","red"), lty=1,lwd=2, bty="n")
dev.off()
